                (CFT) /Obligations of Securities Market Intermediaries under the Prevention
                of Money Laundering Act, 2002 and Rules framed there under.</p>
            <p>The Prevention of Money Laundering Act, 2002 (PMLA) was brought into
                force with effect from 1st July, 2005. As per PMLA, every banking company,
                financial institution (which includes chit fund company, a cooperative bank,
                a housing finance institution and a non-banking financial company) and
                intermediary (which includes a stockbroker, sub-broker, share transfer
                agent, banker to an issue, trustee to a trust deed, registrar to an issue,
                merchant banker, underwriter, portfolio manager, investment adviser and
                any other intermediary associated with securities market and registered
                under section 12 of the Securities and Exchange Board of India Act, 1992)
                shall have to maintain a record of all the transactions; the nature and value
                of which has been prescribed in the Rules notified under the PMLA.</p>
            <p>For the purpose of PMLA, transactions include: </p>
            <ol>
                <li>All cash transactions of the value of more than Rs 10 lakhs or its
                    equivalent in foreign currency. </li>
                <li>All series of cash transactions integrally connected to each other
                    which have been valued below Rs 10 lakhs or its equivalent in foreign
                    currency, such series of transactions within one calendar month
                </li>
                <li>All suspicious transactions whether or not made in cash and
                    including, inter-alia, credits or debits into from any non-monetary
                    account such as Demat account, security account maintained by the
                    registered intermediary.</li>
            </ol>
            <p>For the purpose of suspicious transactions reporting, apart from
                ‘transactions integrally connected’, ‘transactions remotely connected or
                related’ need to be considered.
            </p>
            <p>“Suspicious transactions” means a transaction whether or not made in cash
                which to a person acting in good faith –</p>
            <ol>
                <li>gives rise to a reasonable ground of suspicion that it may involve the
                    proceeds of crime; or </li>
                <li> appears to be made in circumstances of unusual or unjustified
                    complexity or </li>
                <li>appears to have no economic rationale or bonafide purpose</li>
            </ol>
            <p>Pursuant to the recommendations made by the Financial Action Task Force
                (FATF) on Anti Money Laundering standards, The Securities and Exchange
                Board of India, has from time to time issued circulars directing Intermediaries to adopt Strict Customer
                Due Diligence practices in order to
                prevent Money Laundering.</p>
