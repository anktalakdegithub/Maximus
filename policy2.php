            <h5>What is Money Laundering?</h5>
            <p>
                Money Laundering can be defined as engaging in financial transactions that
                involve income derived from criminal activity, transactions designed to
                conceal the true origin of criminally derived proceeds and appears to have
                been received through legitimate sources/origins
            </p>
            <p> In simple terms money laundering is most often described as the “turning of
                dirty or black money into clean or white money”. If undertaken successfully,
                money laundering allows criminals to legitimize "dirty" money by mingling it
                with "clean" money, ultimately providing a legitimate cover for the source of
                their income. </p>
            <p>Section 3 of the PMLA Act defines money laundering in following words:</p>
            <p>“Whosoever directly or indirectly attempts to indulge or knowingly assists or knowingly is a
                party or is actually involved in any process or activity connected with the proceeds of crime
                and projecting it as untainted property shall be guilty of offence of money-laundering”.</p>
            <h5>Prevention of Money Laundering Act, 2002</h5>
            <p>Prevention of Money Laundering Act, 2002 (PMLA 2002) forms the core of
                the legal framework put in place by India to combat money laundering.
                PMLA 2002 and the Rules notified there under came into force with effect
                from July 1, 2005.</p>
            <p>The PMLA 2002 and Rules notified there under impose an obligation on
                intermediaries (including Depository participants, stock brokers and subbrokers) to verify identity of
                clients, maintain records and furnish
                information to the Financial Intelligence Unit (FIU) - INDIA </p>
            <h5>Financial Intelligence Unit (FIU) – INDIA</h5>
            <p>The Government of India set up Financial Intelligence Unit-India (FIU-IND)
                on November 18, 2004 as an independent body to report directly to the
                Economic Intelligence Council (EIC) headed by the Finance Minister. </p>
            <p>FIU-IND has been established as the central national agency responsible for
                receiving, processing, analysing and disseminating information relating to
                suspect financial transactions. FIU-IND is also responsible for coordinating
                and stretching efforts of national and international intelligence and
                enforcement agencies in pursuing the global efforts against money
                laundering and related crimes.</p>
