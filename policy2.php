            <h6>14. PARAMETERS FOR ANALYSING THE TRANSACTIONS:</h6>
            <ul>
                <li>Analysis of top clients in terms of turnover:</li>
                <li>On a periodic basis identify top 200 clients on descending order of
                    turnover and ascertain whether the same matches with the financial
                    status of the client. Moreover, analyze whether there is commensurate
                    fund movement, whether the payments are being made, in cheque and
                    from the bank account(s) of the clients registered with MSL etc. To the
                    extent possible, obtain the latest Income Tax return of the client to
                    ascertain the financial capacity of the client.
                </li>
                <li>Analysis of top client in terms of increase in turnover:</li>
                <li>On a periodic basis identify the top 100 clients, on descending order of
                    increase in turnover over a threshold amount of Rupees One Crore.
                    Then analyze whether the same matches with the financial status of the
                    client, whether there are commensurate funds movement, whether the
                    payments are being made through cheque from the bank account(s) of
                    the client, etc. To the extent possible, we need to obtain the latest
                    Income Tax return of the client to ascertain the level of income of the
                    client.
                </li>
                <li>Matched trades (where the buy and sell clients are with the same broker)</li>
                <li>On a daily basis identify the matched trades (where the buy and sell
                    orders are from same/different branches of MSL in B1, B2 & group and
                    penny stocks). Further analyze whether the client are indulging in any
                    sort of manipulation like shifting of profits from one account to another,
                    creating artificial volumes, circular trading, indulging in price
                    manipulation, shifting the beneficial ownership of shares, etc.</li>
                <li>Analysis of trades in stocks less than Rs.10/- and beyond a specific quantity</li>
                <li>On a daily basis analyze the trades done in penny stocks (stocks less
                    than Rs.10/-). This will help us in identifying whether the client is
                    indulging in any sort of manipulation like creation of artificial volume,
                    artificial price movement, circular trading, etc.</li>
                <li>Client concentration in particular scrip or select scrips</li>
                <li>On a periodic basis, ascertain whether any client is concentrating on
                    any particular scrip, especially liquid and penny stocks. This will help in
                    identifying whether the client is indulging in any sort of manipulation
                    like creation of artificial volume, artificial price movement, circular
                    trading, etc. or is having insider information.</li>
                <li>Analysis of trades in illiquid counter (illiquidity can be with reference to
                    the average volume in the scrip over the last 3 months below a
                    particular level) </li>
                <li>A daily analysis of the client’s volume with respect to the total traded
                    volume at the exchange has to be done. (TTQ analysis). This will help in
                    identifying whether the client is indulging in any sort of manipulation
                    like creation of artificial volume, artificial price movement, circular
                    trading, etc.
                </li>
                <li>Trades shifted from one client to another within the same broker</li>
                <li>On a daily basis, analyze the details of trade shifted from one client to
                    another, either during the market hours or after the market hours. This
                    will help us in identifying whether the client is trying to hide his identity,
                    indulging in profit/loss transfer, availing exposure while in debit
                    balance, etc.</li>
                <li>Analysis of NRI/Foreign clients/institutional trades especially w.r.t.
                    payment patterns
                    <p>Undertake a periodic analysis of the trades executed by NRI/Foreign
                        clients/institutional clients. It should be supplemented by analysis of the payment
                        patterns, trading patterns, etc.</p>
                    <p>With respect to the institutions, analyze all the cancelled trades and DVP
                        trades to see whether the institutional code is being misused. In such cases,
                        also ascertain who the ultimate beneficiary of the trades.</p>
                </li>
                <li>Analysis of client payments in DD, Pay order, cash etc.
                    <p>Undertake a periodic analysis of all the payments received from client by
                        way ofDD/Pay order. This will help in ascertaining whether any client’s DD/
                        Pay Orders have been purchased against “Cash” or might whether any client
                        is making third party payments. The third party payments can relate to
                        employees also.
                    </p>
                </li>
                <li>Pro trade analysis:
                    <p>Currently, MSL does not undertake in pro-trade. Majority of the trades
                        reflected in the pro- account are error trades. A monthly audit of the trades
                        reflected in the pro account has to be done to ascertain the nature of such
                        trade.</p>
                </li>
                <li> Employees/Sub broker account trades
                    <p>Need to analyze the trades undertaken in the employees or sub broker’s
                        own trading account. It is possible that the employees/sub brokers might
                        be accepting cash from the clients and execute the trades in their account.
                        Similarly, it is possible that the profit on trades of the clients being shifted
                        to the employees/sub broker’s accounts.</p>
                </li>
                <li>Frequent change in client details like bank account, demat account,
                    address, etc
                    <p>A monthly analysis of changes in the client details has to be undertaken to
                        identify the clients who are making frequent changes whether the client
                        trying to hide or confuse his identity.
                    </p>
                </li>


            </ul>
