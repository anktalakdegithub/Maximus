            <p>8.Risk Profiling Of The Client</p>
            <p>We should accept the clients based on the risk they are likely to pose. The
                aim is to identify clients who are likely to pose a higher than average risk of
                money laundering or terrorist financing. For this purpose, we need to
                classify the clients as Low risk, medium risk and high-risk clients. By
                classifying the clients, we will be in a better position to apply appropriate customer due diligence
                process. That is, for high risk client we have to apply
                higher degree of due diligence. The factors of risk perception depend on
                client’s location, nature of business activity, turnover, nature of transaction,
                manner of payment etc. </p>
            <p>In order to achieve this objective, all clients should be classified in the
                following category :</p>
            <ul>
                <li>Category A – Low Risk </li>
                <li>Category B – Medium Risk</li>
                <li>Category C – High risk</li>
            </ul>
            <p>Category A clients are those pose low or nil risk. They are good
                corporate/HNIs who have a respectable social and financial standing. These
                are the clients who make payment on time and take delivery of shares.
            </p>
            <p>Category B clients are those who are intra-day clients or speculative clients.
                These are the clients who maintain running account with MSL. </p>
            <p>Category C clients are those who have defaulted in the past, have suspicious
                background, do not have any financial status, etc. </p>
            <p>We have to be careful while monitoring the transactions of B and C category
                clients. Apart from this we need to exercise extra caution while monitoring
                the transactions of NRI/NRE/PIO and foreign clients, especially when the
                payment is being made in foreign currency.</p>
