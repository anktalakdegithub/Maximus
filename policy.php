marked Inactive on account of Rules, Bye laws, circulars and guidelines issued by Sebi, Exchanges and 
Internal Risk Management Policies.</p>
<p class="text-justify text-dark">
    <h6 class="text-dark">Background:</h6>
SEBI vide circular no. dated December 3, 2009 and National Stock Exchange vide circular no.
NSE/INSP/13606 dated December 3, 2009 and NSE/INSP/14048 dated February 3, 2010 and circular no. 
NSE/INSP/46506 dated February 10, 2020 directed that a policy be framed by stock brokers to deal with 
the inactive/dormant accounts.
</p>
<p class=" text-dark">
    <h6 class="text-dark">Policy:</h6>
<b>Procedure to handle Inactive/dormant accounts:</b>
<b>Temporarily Suspending or Closing a Client's account at the client’s request:</b>
Maximus Securities Ltd may carry out periodic review of the client accounts and may suspend the 
accounts from trading (i.e. prohibiting any market transactions, only allowing client shares/ledger 
balance settlement to take place) under any of the following circumstances:

</p>
<ul style="text-align: initial;">
<li> Where the Client is inactive for the period of one year or more.</li>
<li> Where the Client has not cleared his dues after repeated reminders</li>
<li> Where Physical statements or contract notes, etc. are received back undelivered and the
 client is not responding to update the correct address.</li>
 <li> Where the client is reported or known to have expired.</li>
 <li> Where client lodges a complaint either directly with SFSL or through the Exchange relating
 to alleged unauthorized trades being executed in his account.</li>
 <li> Where the account in under investigation by any regulatory body.</li>
 <li> As per direction of the Exchanges, SEBI or any other regulatory body.</li>
 <li> On written request received from the client and the same can be activated on the written
 request of the client only</li>
</ul>
<p>The Client account can be closed on the written request of the client provided the client account
is settled. If the client wants to reopen the account then the client has to again complete the
KYC requirement.</p>
<p class="text-left">Client account will be considered as inactive if the client does not trade for a period of 1 year.
    <br>
All the accounts nominated as inactive / dormant needs to be monitored carefully in order to
avoid unauthorized transactions in the account. If the client wants to make the account active
after 1 year or there after needs to provide the required documents towards supporting the financial 
