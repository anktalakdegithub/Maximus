in the information such as; address, mobile number, email id, bank / demat account, financial disclosure
provided in KYC at the time of registration as client, the same has to be submitted along with the 
request. After proper verification of the updated / revised details and approval from the compliance 
officer / or concerned department in-charge of registration of clients, the account can be made active 
and transaction can take place.</p>
