<b>Consequences of Inactive Account
</b>
<p class="text-left">
On a client being declared inactive, the client’s funds and demat account shall be settled.
Settlement of client account needs to be done as per the periodicity (monthly/quarterly) opted by
the client and his/her assets (funds, securities or any other collateral) be returned to him/her and
statement needs to be sent to client. Proof of sending the statements of settlement of accounts
has to be maintained. Settlement of client account needs to be done at least once in a calendar
quarter. In case of interim request received from the client for release of funds/securities, the
funds and/or securities will be transferred to his account after due verification of the client as per
the procedure mentioned above.
</p>
